export class GarageDoor {
    up() {
        console.log("Garage Door is Open");
    }

    down() {
        console.log("Garage Door is Close");
    }

    stop() {
        console.log("Garage door stop");
    }

    lightOn() {
        console.log("Garage door on");
    }

    lightOff() {
        console.log("Garage door off");
    }
}